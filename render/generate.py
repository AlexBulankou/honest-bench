"""Aggregate both products' results into the public top-level README (Layer-1 render).

This is the file-writer companion to render.py's per-product `render_product`. It reads
each product's schema-validated `results/latest.json`, renders its closed-schema table,
and writes the ENTIRE top-level README.md from a fixed preamble constant + the generated
tables. The README is 100%-generated by construction: there is no hand-edited region a
reviewer could later slip free-text into, so the closed-schema guard (render.py) is the
only path from harness output to the public page.

Invoked by the auto-refresh Action (#3866) as `python3 -m render.generate` from the repo
root, and runnable directly as `python3 render/generate.py`.

Import note: under `-m render.generate` the `render/` DIRECTORY is bound as a namespace
package named `render`, which shadows `render.py` — so a flat `from render import ...`
resolves to the directory (no `render_product` attribute) and fails. We therefore load
render.py by explicit file path via importlib, which is invariant across all invocations
(`-m render.generate`, `python3 render/generate.py`, `cd render && python3 generate.py`)
and needs no `__init__.py` (which would break the dependency-free direct-run tests). The
sys.path insert still puts render/ first so render.py's own flat `from schema import ...`
resolves.
"""

import importlib.util
import json
import os
import sys

_HERE = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, _HERE)


def _load_render_product():
    spec = importlib.util.spec_from_file_location("_bench_render", os.path.join(_HERE, "render.py"))
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod.render_product


render_product = _load_render_product()

# Product -> results path, relative to the repo root (parent of render/).
_PRODUCTS = (
    ("sandbox", "sandbox/results/latest.json"),
    ("substrate", "substrate/results/latest.json"),
)

# Fixed, reviewed-once customer preamble. No measured numbers here — every number on the
# page comes from render_product, so this constant carries zero PII risk.
_PREAMBLE = """\
# Honest benchmarks — GKE agent sandbox & substrate

These tables are **machine-rendered, never hand-entered**. A measured cell traces to a
schema-validated field of a real harness run; anything the schema does not declare is
dropped before it can reach this page. We publish only what we actually measured on the
substrate named in each build banner — a `kind` run is labelled `kind`, so a local number
is never presented as a production SLA.

A cell we have not yet measured renders `pending (<reason>)` — never a guess and never a
false PASS/FAIL. The **sandbox** table below is measured against a live GKE cluster — the
`cluster_substrate=gke-sandbox` build banner names the substrate, and the burst-create
headline was measured with the gVisor (runsc) runtime. The two NetworkPolicy cells are
qualified `(control-plane)` because a PASS there asserts the policy was admitted and
correctly targeted, not that data-plane traffic was enforced. The **substrate** table is
still seed state — every cell reads `pending (not-yet-measured)` until its first measured
run.

Every measured number here is a **reproducible floor, not a ceiling.** It is what a
*vanilla* OSS build — the upstream controller from `main`, default runtime, no tuning,
the cluster shape named in the build banner — delivers today. A bigger pool, denser nodes,
or a tuned runtime should *beat* it; the value on the page is the honest lower bound you can
reproduce from the recipe below and then improve on. We publish the floor we can stand
behind, not the best number we could cherry-pick.

- **Measured (N)** is the value we observed, with the sample size.
- **Committed / Target / North-Star** render `(non-public)` here by construction — internal
  goal numbers never ship to this repo.

Reproduce any row yourself — then beat it. The suite is honest by construction:

```
bash recipe/install-controller-from-main.sh   # OSS controller from upstream main
python3 -m harness.run                        # run the portable suite (substrate=kind)
python3 -m render.generate                    # regenerate these tables
bash scripts/check-public-safety.sh           # fail-closed public-safety scan
```
"""


def _repo_root():
    return os.path.dirname(os.path.dirname(os.path.abspath(__file__)))


def build_readme(root=None):
    """Return the full README text: preamble + each present product's rendered table."""
    root = root or _repo_root()
    sections = [_PREAMBLE.rstrip()]
    for product, rel in _PRODUCTS:
        path = os.path.join(root, rel)
        if not os.path.exists(path):
            continue
        with open(path) as fh:
            results = json.load(fh)
        sections.append(render_product(results).rstrip())
    return "\n\n".join(sections) + "\n"


def main(argv=None):
    root = _repo_root()
    readme = build_readme(root)
    out = os.path.join(root, "README.md")
    with open(out, "w") as fh:
        fh.write(readme)
    sys.stderr.write(f"generate: wrote {out} ({len(readme)} bytes)\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
